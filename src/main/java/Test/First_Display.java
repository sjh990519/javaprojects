    private String getServerIp() {

        InetAddress local = null;
        try {
            local = InetAddress.getLocalHost();
        } catch (UnknownHostException e) {
            System.out.println("error");
        }

        if (local == null) {
            return "";
        } else {
            ip = local.getHostAddress();
            return ip;
        }
    }

    public First_Display(String ip, int port) { //소켓 생성을 위한 클래스 생성자
        initComponents();
        setTitle("로그인");
        try {
            socket = new Socket(ip, port);
            in = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            out = new PrintWriter(new OutputStreamWriter(socket.getOutputStream()));
        } catch (IOException e) {
            System.out.println(e.toString());
        }
    }

    public void send_ID(String ID) { // ID값을 서버에 전송하는 메소드
        out.println(ID);
        out.flush();

    }

    public void send_PW(String PW) { // PW값을 서버에 전송하는 메소드
        out.println(PW);
        out.flush();

    }

    public void send_Op(String Radio) { // 라디오값을 서버에 전송하는 메소드
        out.println(Radio);
        out.flush();

    }

    public void receive() { //서버로부터 받은 값(로그인 성공 여부)를 정적변수인 status에 대입.
        try {

            status = in.readLine();
            System.out.println("\n--------------------------");
            System.out.println("서버로부터 받은 로그인여부 값");
            System.out.println("[서버] " + status);
            System.out.println("--------------------------\n");
        } catch (IOException e) {
            System.out.println(e.toString());
        }

    }

