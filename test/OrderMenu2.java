    public void Order(String str) {
        File menu_file = new File(str);//C:\netbeans\h위치

        if (menu_file.exists()) {//파일이 존재하면 
            System.out.println("있다.");
        } else {
            System.out.println("없다.");
        }

        try {
            FileReader filereader = null;
            filereader = new FileReader(menu_file);

            BufferedReader bufReader = new BufferedReader(filereader);
            //jTable1 table = new 
            DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
            table.setNumRows(0);// 테이블 초기화래요.

            String line;
            String[] key;

            // 한 행씩 읽어서 한 행씩 테이블에 저장
            while ((line = bufReader.readLine()) != null) {
                key = line.split("/");
                Object[] list = {key[0], key[1], key[2]};
                table.addRow(list);
            }
        } catch (Exception e) {
            e.getStackTrace();
        }
    }//Order

